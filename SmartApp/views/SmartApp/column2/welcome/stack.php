	<h3 id="means">Back End Technology Stack</h3>

	<ul class="SmartApp-items">
		<li><span class="SmartApp-item-title">PHP</span>: Q runs on pretty much any environment with PHP 5 or above.</li>
		<li><span class="SmartApp-item-title">MySQL</span>: Currently, Q needs MySQL to store data, but other adapters are coming soon.</li>
		<li><span class="SmartApp-item-title">Node.js</span>: Optional but highly recommended, enables real time updates, notifications and more.</li>
	</ul>
	
	<h3 id="means">Front End Technology Stack</h3>

	<ul class="SmartApp-items">
		<li><span class="SmartApp-item-title">Browsers</span>: Q comes with libraries to quickly build extremely efficient web apps for desktop or mobile browsers.</li>
		<li><span class="SmartApp-item-title">PhoneGap</span>: Also known as Cordova, it can be used to build native mobile apps with Q, which integrate with the phone's contact list and more.</li>
		<li><span class="SmartApp-item-title">jQuery</span>: Some of Q's plugins make use of jQuery on the front end.</li>
		<li><span class="SmartApp-item-title">Socket.io</span>: Connects with Node.js on the back end to enable real time updates in the user interface.</li>
	</ul>

