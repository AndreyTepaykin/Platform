		<h3 id="about">Developers and Teams</h3>

		<p>
			Q was built from the ground up to help development teams be more productive.
		</p>
		<ul class="SmartApp-items">
			<li>
				<span class="SmartApp-item-title">Build once, run everywhere</span>:
				Apps built on Q can run on desktop browsers, tablets and mobile phones practically out of the box.
				A unified platform allows for easier maintenance.
			</li>
			<li>
				<span class="SmartApp-item-title">Re-use your code</span>:
				Q is extremely modular. In fact, once you are done with your app, you can easily
				package any new functionality you've built as a plugin, to be installed and re-used
				in any other app. You will even be able to develop and sell plugins in our marketplace.
			</li>
			<li>
				<span class="SmartApp-item-title">Support many users</span>:
				We've done all the hard work of handling user registration, authentication,
				address book import, authentication, etc. Focus on writing your app, not reinventing the wheel.
			</li>
			<li>
				<span class="SmartApp-item-title">Real-time collaboration</span>:
				Q goes far beyond REST-based resource management. Just like Unix lets you work using <em>files</em>,
				Q lets you work using <em>streams</em>, which support joining, leaving, posting messages,
				real-time updates, subscriptions and notifications, and granular access control that integrates
				seamlessly with the user's contacts. People can, for example, invite their friends to a chess game,
				get updates in real time, and set up alerts on their phone. All you have to do is write the
				front-end interface to render and play the chess game.
			</li>
			<li>
				<span class="SmartApp-item-title">Scalable</span>:
				Unlike other web platforms, Q is ready to handle the day your app makes it big.
				The database layer automatically supports sharding, and can smoothly scale your back end
				even as your app stays online, maintaining 99.99% uptime.
			</li>
			<li>
				<span class="SmartApp-item-title">Efficient</span>: Thanks to its design choices, Q is
				very efficient. It minimizes round-trips to the server, and supports caching at all layers.
				But since it uses
				<a target="_blank" href="http://socket.io">socket.io</a>,
				the caches are almost never out of date.
			</li>
			<li>
				<span class="SmartApp-item-title">Flexible</span>: We know that many platforms make it hard to
				achieve exactly what you need. That's why you can override almost anything in Q.
				You can also easily import classes from other platforms such as
				<a target="_blank" href="http://platform.zend.com/">Zend</a> and our autoloader
				won't get jealous &mdash; it's designed to play nice with others.
			</li>
			<li>
				<span class="SmartApp-item-title">Built for teams</span>:
				Q is designed for teams of developers, designers, testers and sysadmins to work together.
				Each team member can configure their local environment, with their own database credentials,
				facebook api keys, paths and so forth, while Q's installer keeps track of versions and migrations
				for each app and plugin. Designers can focus on themes, and testers can focus on tests.
			</li>
		</ul>
		
